//for, while,do-while
function Sequence() {
    console.log("Sequence Hello");
    console.log("Sequence Hello");
    console.log("Sequence Hello");
    console.log("Sequence Hello");
    console.log("Sequence Hello");
}
function For() {
    var i = 0;
    for (i = 1; i <= 5; i++) {
        console.log("For Hello");
    }
}
function While() {
    var i = 0;
    while (i <= 5) {
        console.log("While Hello");
        i++;
    }
}
Sequence();
For();
While();
